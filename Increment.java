
public class Increment {
	
	public static void main(String args[]) {
		
		int i=5;
		int j=i++;
		System.out.println("Values : "+i+" and "+j);
		
		int k=5;
		k=k++;
		System.out.println(" Value : "+k);
		
		int l=5;
		int m=++l;
		System.out.println("Values : "+l+" and "+m);
		
	}

}
